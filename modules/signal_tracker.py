def record_signal(symbol: str, data: dict, score_result: dict) -> str:
    """
    Record a new alert signal for 15-day monitoring.
    Saves COMPLETE snapshot of all 60+ data fields at alert time.
    Returns signal_id.
    """
    _ensure_dirs()

    signal_id = f"{symbol}_{datetime.now(timezone.utc).strftime('%Y%m%d_%H%M%S')}"
    now_str   = datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")
    price     = data.get("price") or 0

    # Fields to exclude from snapshot (internal/redundant/too large)
    EXCLUDE_FIELDS = {
        "signals", "reasons", "penalties",  # stored separately
        "price_checks", "notifications_sent",  # tracker fields
        "ob_exchange_depth", "oi_exchanges",
        "funding_per_exchange", "ls_per_exchange",  # large dicts
    }

    # Save COMPLETE snapshot — every field the scanner collected
    full_snapshot = {}
    for k, v in data.items():
        if k in EXCLUDE_FIELDS:
            continue
        # Serialize safely
        if isinstance(v, (str, int, float, bool, type(None))):
            full_snapshot[k] = v
        elif isinstance(v, (list, dict)):
            try:
                json.dumps(v)  # test if serializable
                full_snapshot[k] = v
            except Exception:
                full_snapshot[k] = str(v)[:200]
        else:
            try:
                full_snapshot[k] = float(v)
            except Exception:
                full_snapshot[k] = str(v)[:200]

    signal = {
        # ── Identity ──────────────────────────────────────────────
        "signal_id":    signal_id,
        "symbol":       symbol,
        "alert_time":   now_str,
        "alert_ts":     time.time(),
        "entry_price":  price,

        # ── Scoring ───────────────────────────────────────────────
        "score":        score_result.get("score"),
        "max_score":    score_result.get("max_score"),
        "strength":     score_result.get("strength"),
        "pct_score":    score_result.get("pct_score"),
        "has_momentum": score_result.get("has_momentum", False),
        "momentum_score": score_result.get("momentum_score", 0),

        # ── All triggered signals with point values ───────────────
        "signals_state": {
            k: {"triggered": bool(v[0]), "points": v[1]}
            if isinstance(v, (list, tuple)) and len(v) >= 2
            else {"triggered": bool(v), "points": 0}
            for k, v in score_result.get("signals", {}).items()
        },

        # ── Human-readable reasons list ───────────────────────────
        "reasons":   score_result.get("reasons", []),
        "penalties": score_result.get("penalties", []),

        # ── COMPLETE data snapshot (all 60+ fields) ───────────────
        "snapshot": full_snapshot,

        # ── Exchange aggregation sources ──────────────────────────
        "data_sources": {
            "oi_source":       data.get("oi_source"),
            "funding_source":  data.get("funding_source"),
            "ls_source":       data.get("ls_source"),
            "liq_source":      data.get("liq_source"),
            "ob_source":       data.get("ob_source"),
            "ob_exchanges":    data.get("ob_exchanges", []),
        },

        # ── 15-day tracking fields (updated each scan) ────────────
        "price_checks":       [],
        "max_pump_pct":       0.0,
        "max_dump_pct":       0.0,
        "peak_price":         price,
        "trough_price":       price,
        "peak_time":          now_str,
        "trough_time":        now_str,
        "notifications_sent": [],
        "status":             "active",
        "completed_time":     None,
        "final_result":       None,
        "expire_ts":          time.time() + MONITOR_DAYS * 86400,
    }

    # Add to active.json
    active = _load_json(ACTIVE_PATH, {})
    active[signal_id] = signal
    _save_json(ACTIVE_PATH, active)

    log.info(f"Signal recorded: {signal_id} entry=${price} score={signal['score']}")
    return signal_id


