def get_stats() -> dict:
    """Calculate accuracy stats from completed signals."""
    completed = _load_json(COMPLETED_PATH, {})
    active    = _load_json(ACTIVE_PATH, {})

    if not completed:
        return {
            "total_completed": 0,
            "active":          len(active),
            "pumped":          0,
            "dumped":          0,
            "flat":            0,
            "win_rate":        0,
            "avg_pump":        0,
            "best_pump":       0,
            "worst_dump":      0,
        }

    signals   = list(completed.values())
    pumped    = [s for s in signals if s.get("final_result") == "pumped"]
    dumped    = [s for s in signals if s.get("final_result") == "dumped"]
    flat      = [s for s in signals if s.get("final_result") == "flat"]
    avg_pump  = sum(s["max_pump_pct"] for s in pumped) / len(pumped) if pumped else 0
    best_pump = max((s["max_pump_pct"] for s in signals), default=0)
    worst     = min((s["max_dump_pct"] for s in signals), default=0)

    # Per-score-range breakdown
    score_buckets = {}
    for s in signals:
        score = s.get("score", 0)
        bucket = f"{(score // 3) * 3}-{(score // 3) * 3 + 2}"
        if bucket not in score_buckets:
            score_buckets[bucket] = {"total": 0, "pumped": 0}
        score_buckets[bucket]["total"] += 1
        if s.get("final_result") == "pumped":
            score_buckets[bucket]["pumped"] += 1

    return {
        "total_completed": len(signals),
        "active":          len(active),
        "pumped":          len(pumped),
        "dumped":          len(dumped),
        "flat":            len(flat),
        "win_rate":        round(len(pumped) / len(signals) * 100, 1),
        "avg_pump":        round(avg_pump, 1),
        "best_pump":       round(best_pump, 1),
        "worst_dump":      round(worst, 1),
        "score_buckets":   score_buckets,
    }


def get_active_signals() -> list:
    active = _load_json(ACTIVE_PATH, {})
    return sorted(active.values(), key=lambda x: x.get("alert_ts", 0), reverse=True)


def get_completed_signals(limit: int = 20) -> list:
    completed = _load_json(COMPLETED_PATH, {})
    signals   = sorted(completed.values(),
                       key=lambda x: x.get("completed_time", ""), reverse=True)
    return signals[:limit]


def get_lifetime_batch_paths() -> list:
    """Return all lifetime batch file paths."""
    _ensure_dirs()
    paths = []
    batch_num = 1
    while True:
        path = f"{LIFETIME_DIR}/batch_{batch_num:03d}.json"
        if os.path.exists(path):
            paths.append(path)
            batch_num += 1
        else:
            break
    return paths


def get_daily_file_path(date_str: str = None) -> Optional[str]:
    """Get path to daily summary file. Default = today."""
    if not date_str:
        date_str = datetime.now(timezone.utc).strftime("%Y-%m-%d")
    path = f"{DAILY_DIR}/{date_str}.json"
    return path if os.path.exists(path) else None
