"""
MODULE: signal_tracker.py

Tracks every alert generated by the bot for 15 days.
Monitors price every scan and records outcomes.
Generates daily summary files.
Stores everything in JSON.

DATA STORED PER SIGNAL:
  - Full snapshot of all 60+ data fields at alert time
  - All 28 signal states at alert time
  - Score and reasons
  - Entry price
  - Price checks every scan for 15 days
  - Final outcome: max pump %, max dump %, result
  - Daily notifications if significant moves happen

FILES:
  data/signal_tracker/
    active.json          — signals currently being monitored
    completed.json       — signals that completed 15-day monitoring
    daily/YYYY-MM-DD.json — all signals that completed on this day
    lifetime/batch_001.json, batch_002.json ... — all signals in chunks of 100

TELEGRAM COMMANDS:
  /signals_active    — show all currently monitored signals
  /signals_history   — show last 20 completed signals
  /signals_stats     — accuracy stats
  /give_file         — sends JSON files of all lifetime signals (100 per file)
"""

import os
import json
import time
from datetime import datetime, timezone, timedelta
from typing import Optional
from modules.logger import get_logger
import config

log = get_logger("signal_tracker")

BASE_DIR     = "data_ls/signal_tracker"
ACTIVE_PATH  = f"{BASE_DIR}/active.json"
COMPLETED_PATH = f"{BASE_DIR}/completed.json"
DAILY_DIR    = f"{BASE_DIR}/daily"
LIFETIME_DIR = f"{BASE_DIR}/lifetime"
MONITOR_DAYS = 15


def _ensure_dirs():
    for d in [BASE_DIR, DAILY_DIR, LIFETIME_DIR]:
        os.makedirs(d, exist_ok=True)


def _load_json(path: str, default):
    try:
        if os.path.exists(path):
            with open(path) as f:
                return json.load(f)
    except Exception as e:
        log.warning(f"Failed to load {path}: {e}")
    return default


def _save_json(path: str, data) -> None:
    try:
        _ensure_dirs()
        with open(path, "w") as f:
            json.dump(data, f, indent=2, default=str)
    except Exception as e:
        log.error(f"Failed to save {path}: {e}")


# ══════════════════════════════════════════════════════════════════════
#  RECORD NEW SIGNAL
